    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            if(edit == true)
            {
                Gebruiker g = new Gebruiker();
                g.setId(Long.parseLong(txtID.getText()));
                g.setNaam(txtGebruikersnaam.getText());
                g.setPassword(txtWachtwoord.getText());
                boolean result = update.UpdateGebruikers(g);
                if(result == true)JOptionPane.showMessageDialog(null, "Update gelukt");
                else JOptionPane.showMessageDialog(null, "update niet gelukt");
            }
            else if(edit == false)
            {
                Gebruiker g = new Gebruiker();
                g.setNaam(txtGebruikersnaam.getText());
                g.setPassword(txtWachtwoord.getText());
                int result = opslaan.OpslaanGebruiker(g);
                if(result == 1)
                {
                    JOptionPane.showMessageDialog(null, "Er was een probleem");
                }
                else if (result == 2)
                {
                    JOptionPane.showMessageDialog(null, "Bestaat reeds");
                }
                else 
                {
                    JOptionPane.showMessageDialog(null, "Opslage geslaagd");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Onbekende fout");
            }
            
            txtGebruikersnaam.setEnabled(false);
            txtWachtwoord.setEnabled(false);
            btnEditGebruiker.setVisible(true);
            btnNewGebruiker.setVisible(true);
            btnSave.setVisible(true);
            txtGebruikersnaam.setText("");
            txtID.setText("");
            txtWachtwoord.setText("");
            RefreshList();
           
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditGebruikerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditGebruikerActionPerformed
        if(lsbGebruiker.getSelectedIndex() != -1)
        {
            txtGebruikersnaam.setEnabled(true);
            txtWachtwoord.setEnabled(true);
            btnEditGebruiker.setVisible(false);
            btnNewGebruiker.setVisible(false);
            btnSave.setVisible(true);
            edit = true;
        }
        else 
        {
            JOptionPane.showMessageDialog(null, "Geen item geselecteerd");
        }
    }//GEN-LAST:event_btnEditGebruikerActionPerformed

