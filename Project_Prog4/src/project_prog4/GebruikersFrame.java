    private void RefreshList()
    {
        try 
            {
                naam.clear();
                List<Gebruiker> Gebruikerlist = aanmakenList.ListGebruiker();
                for(Gebruiker g: Gebruikerlist)
                {
                    naam.add(g.getNaam());
                }
                lsbGebruiker.setListData(naam.toArray());
        
            } 
        catch (Exception e) {}
    }
    
    

