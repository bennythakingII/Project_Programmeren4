    private void RefreshList() {
        
        try
        {
            namen.clear();
            List<Gebruiker> listGebruiker = listmaken.ListGebruiker();
            for ( Gebruiker g : listGebruiker)
            {
                namen.add(g.getNaam());
            }
            lsbGebruiker.setListData(namen.toArray());
            lsbGebruiker.setSelectedIndex(0);
            setTextbox();
        }
        
        catch(Exception ex)
        {}
        
    }

    private void setTextbox() {
        if(lsbGebruiker.getSelectedIndex() != -1)
        {
            Gebruiker result = z.ZoekGebruikerDoorNaam(namen.get(lsbGebruiker.getSelectedIndex()));
            txtNaam.setText(result.getNaam());
            txtPassword.setText(result.getPassword());
            txtID.setText(Long.toString(result.getId()));
        }
    }
