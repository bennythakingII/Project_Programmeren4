    private void btnOpslaanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpslaanActionPerformed
        try
        {
            if(edit == true)
            {
                Gebruiker g = new Gebruiker();
                g.setId(Long.parseLong(txtID.getText()));
                g.setNaam(txtNaam.getText());
                g.setPassword(txtPassword.getText());
                
                int result = opslaan.OpslaanGebruiker(g);
                if(result == 1) JOptionPane.showMessageDialog(null, "Update geslaagd!" + g);
                else if (result == 2)JOptionPane.showMessageDialog(null, "Update niet geslaagd, Naam bestaad al");
                else JOptionPane.showMessageDialog(null, "Update is niet geslaagd");
                
            }
            
            else if (edit == false)
            {
                Gebruiker g = new Gebruiker();
                g.setNaam(txtNaam.getText());
                g.setPassword(txtPassword.getText());
                int result = opslaan.OpslaanGebruiker(g);
                
                
            }
            
            RefreshList();
        }
        
        catch(Exception ex)
        {}
    }//GEN-LAST:event_btnOpslaanActionPerformed

